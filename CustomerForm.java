    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // Edit
        int i=table1.getSelectedRow();
        if(i!=-1){
            String custno=table1.getValueAt(i, 0)+"";
            String custname=table1.getValueAt(i, 1)+"";
            String custaddress=table1.getValueAt(i, 2)+"";
            String mobileno=table1.getValueAt(i, 3)+"";
            String bdate=table1.getValueAt(i, 4)+"";
            String acbal=table1.getValueAt(i, 5)+"";
            txtno.setText(custno);
            txtname.setText(custname);
            txtaddress.setText(custaddress);
            txtmobile.setText(mobileno);            
            txtbdate.setDate(LocalDate.parse(bdate));
            txtbal.setText(acbal);
            btnsave.setText("Update");
            txtname.requestFocus();            
        }else{
            Messages.showError("Please select row", "Row is Not Selected");
        }
        
    }//GEN-LAST:event_btneditActionPerformed

    private void btnedit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnedit1ActionPerformed
        //Delete
        int i=table1.getSelectedRow();
        if(i!=-1){
            int ans=Messages.showConfirm("Are you sure?", "Confirm Delete");
            if(ans==JOptionPane.YES_OPTION){
                String custno=table1.getValueAt(i, 0)+"";                
                db.execute("delete from Customer where CustNo=?", custno);
                db.fillTable(table1, "SELECT * from Customer");
            }
        }else{
            Messages.showError("Please select row to remove", "Row Not Selected");
        }

    }//GEN-LAST:event_btnedit1ActionPerformed

