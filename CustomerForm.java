    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // Save
        String custno=txtno.getText();
        String custname=txtname.getText();
        String custaddress=txtaddress.getText();
        String mobile=txtmobile.getText();
        if(txtbdate.getText().equals("")){
            Messages.showWarning("Birth Date cannot be left empty", "Input Error");
            txtbdate.requestFocus();
            return;
        }
        String bdate=txtbdate.getDateStringOrEmptyString(); //yyyy-MM-dd
        String acbal=txtbal.getText();
        String btntext=btnsave.getText(); //Save Update
        //Apply Validation
        if(custname.equals("")){
            Messages.showWarning("Customer Name cannot be left empty", "Input Error");
            txtname.requestFocus();
            return;
        }
        if(custaddress.equals("")){
            Messages.showWarning("Customer Address cannot be left empty", "Input Error");
            txtaddress.requestFocus();
            return;
        }
        if(mobile.equals("")){
            Messages.showWarning("Mobile No cannot be left empty", "Input Error");
            txtmobile.requestFocus();
            return;
        }
        if(!Rules.isMobile(mobile)){
            Messages.showWarning("Mobile No must consist of only 10 digits", "Input Error");
            txtmobile.requestFocus();
            return;
        }
        if(acbal.equals("")){
            Messages.showWarning("Account Balance cannot be left empty", "Input Error");
            txtbal.requestFocus();
            return;
        }
        if(!Rules.isDouble(acbal)){
            Messages.showWarning("Account Balance should be numeric", "Input Error");
            txtbal.requestFocus();
            return;
        }
        
        if(btntext.equals("Save")){ //Insert
            db.execute("Insert into Customer values(?,?,?,?,?,?)",custno,custname,custaddress,mobile,bdate,acbal);
            db.updateID("CustNo");
                    
            Messages.showInfo("Customer Information is Saved Successfully...", "Success");        
        }else{ //Update
            db.execute("Update Customer set CustName=?,CustAddress=?,MobileNo=?,BirthDate=?,ACBalance=? where CustNo=?",custname,custaddress,mobile,bdate,acbal,custno);
            Messages.showInfo("Customer Information is Updated Successfully...", "Success");        
        }
        db.fillTable(table1, "select * from Customer");
        
    }//GEN-LAST:event_btnsaveActionPerformed

