    private void btnremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveActionPerformed
        // Delete
        //Get rowindex of table
        int rowindex=table1.getSelectedRow();
        if(rowindex!=-1){ //if row is selected
            int ans=JOptionPane.showConfirmDialog(this, "Are you sure?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
            if(ans==JOptionPane.YES_OPTION){
                String payno=table1.getValueAt(rowindex, 0)+"";            
                String suppno=table1.getValueAt(rowindex, 2)+"";            
                String amt=table1.getValueAt(rowindex, 4)+"";            
                db.execute("delete from CustomerPayment where PayNo=?", payno);
                db.execute("Update Customer set AcBalance=AcBalance+? where CustNo=?",amt,suppno);
                db.fillTable(table1, "select * from CustomerPayment");
            }            
        }else{
            JOptionPane.showMessageDialog(this, "Please select CustomerPayment to remove");
        }
        
        
        
    }//GEN-LAST:event_btnremoveActionPerformed

