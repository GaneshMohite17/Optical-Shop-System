    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // Save
        String payno=txtpayno.getText();
        if(txtpdate.getText().equals("")){
        Messages.showError("Payment Date cannot be left empty..", "Input Error");
        txtpdate.requestFocus();
        return;
        }
        String pdate=txtpdate.getDate().format(DateTimeFormatter.ISO_DATE);
        String id=txtid.getText();
        String name=txtname.getText();
        String amt=txtamt.getText();
        String mode=cmbpaymode.getSelectedItem()+"";
        String details=txtdetails.getText();
        
        
        if(name.equals("")){
        Messages.showError("Customer Name cannot be left empty..", "Input Error");
        txtname.requestFocus();
        return;
        }
        if(amt.equals("")){
        Messages.showError("Amount cannot be left empty..", "Input Error");
        txtamt.requestFocus();
        return;
        }
        if(!Rules.isDouble(amt)){
        Messages.showError("Amout should be numeric..", "Input Error");
        txtamt.requestFocus();
        return;
        }
        db.execute("Insert into CustomerPayment values(?,?,?,?,?,?,?)", payno,pdate,id,name,amt,mode,details);
        db.execute("Update Customer set ACBalance=ACBalance-? where CustNo=?",amt,id);
        db.fillTable(table1, "select * from CustomerPayment");
        db.updateID("CPayNo");
        JOptionPane.showMessageDialog(this, "Customer Payment record is Saved Successfully","Save",JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btnsaveActionPerformed

