    private void btnprintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprintActionPerformed
        HashMap<String,Object> map=new HashMap<>();
        
        if(DBClass.pvalue.equals("")){
            DBClass.pvalue="PayNo like '%%'";
        }
        String ptitle="";
        if(!txtsearch.getText().trim().equals(""))
        {
            ptitle=cmbfield.getSelectedItem()+" like "+txtsearch.getText();
        }
        map.put("param",DBClass.pvalue);
        map.put("paramtitle",ptitle);
        db.showReport("src/reports/CustomerPaymentListReport.jrxml",map);
    }//GEN-LAST:event_btnprintActionPerformed

    private void btnsuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuppActionPerformed
        // Customer Form
        CustomerForm f=new CustomerForm();
        f.table1.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if(e.getClickCount()==1){//single click
                    int i=f.table1.getSelectedRow();
                    if(i!=-1){
                        txtid.setText(f.table1.getValueAt(i, 0)+"");
                        txtname.setText(f.table1.getValueAt(i, 1)+"");
                        lblbal.setText(f.table1.getValueAt(i, 5)+"");
                        f.dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "Please Select Row");
                    }
                }
            }

        });

        f.setVisible(true);
    }//GEN-LAST:event_btnsuppActionPerformed

